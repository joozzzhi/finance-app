def add_heading_custom(text, level=1):
    """Добавить заголовок без отступа абзаца"""
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    if level == 0:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run(text)
        run.bold = True
        run.font.size = Pt(16)
    elif level == 1:
        run = p.add_run(text)
        run.bold = True
        run.font.size = Pt(16)
    elif level == 2:
        run = p.add_run(text)
        run.bold = True
        run.font.size = Pt(14)
    elif level == 3:
        run = p.add_run(text)
        run.bold = True
        run.font.size = Pt(14)
    run.font.name = 'Times New Roman'
    return p

def add_para(text, bold=False, align=None, indent=True, size=14):
    """Добавить абзац"""
    p = doc.add_paragraph()
    if not indent:
        p.paragraph_format.first_line_indent = Cm(0)
    if align:
        p.alignment = align
    run = p.add_run(text)
    run.bold = bold
    run.font.size = Pt(size)
    run.font.name = 'Times New Roman'
    return p

def add_table(headers, rows):
    """Добавить таблицу"""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    # Заголовки
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = ''
        run = cell.paragraphs[0].add_run(h)
        run.bold = True
        run.font.size = Pt(12)
        run.font.name = 'Times New Roman'
        cell.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
    # Данные
    for r_idx, row in enumerate(rows):
        for c_idx, val in enumerate(row):
            cell = table.rows[r_idx + 1].cells[c_idx]
            cell.text = ''
            run = cell.paragraphs[0].add_run(str(val))
            run.font.size = Pt(12)
            run.font.name = 'Times New Roman'
    doc.add_paragraph()  # отступ после таблицы
    return table

def add_figure_placeholder(caption, num):
    """Заглушка для рисунка"""
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.first_line_indent = Cm(0)
    run = p.add_run(f'[Место для рисунка {num}]')
    run.font.size = Pt(12)
    run.font.name = 'Times New Roman'
    run.font.color.rgb = RGBColor(150, 150, 150)
    run.italic = True
    p2 = doc.add_paragraph()
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p2.paragraph_format.first_line_indent = Cm(0)
    run2 = p2.add_run(f'Рисунок {num} – {caption}')
    run2.font.size = Pt(12)
    run2.font.name = 'Times New Roman'

